    public void setPlayer() {
        playerOne = new PlayerOne(ImageUtil.playerTwoR, 50, 50, new Point3f(500, 50, 0), ImageUtil.playerTwoU,
                ImageUtil.playerTwoD, ImageUtil.playerTwoL, ImageUtil.playerTwoR);
        playerList.add(playerOne);
        playerOne.setAlive(true);

        playerTwo = new PlayerTwo(ImageUtil.playerR, 50, 50, new Point3f(500, 700, 0), ImageUtil.playerU,
                ImageUtil.playerD, ImageUtil.playerL, ImageUtil.playerR);
        playerList.add(playerTwo);
        playerTwo.setAlive(true);
    }

    // This is the heart of the game , where the model takes in all the inputs
    // ,decides the outcomes and then changes the model accordingly.
    public void gamelogic() {

        if (MainWindow.state == GameConstants.GAME_START) {
            TankAndPlayerCollideMapTile();
            playerOneLogic();
            enemyLogic();
            // Player Logic first
            if (MainWindow.gameModel == GameConstants.GAME_MODEL_DOUBLE_PLAYER) {
                setMaxEnemyCount(20);
                playerTwoLogic();
            }

            // Bullets move next
            bulletLogic(enemyBulletsList);
            bulletLogic(BulletList);
            // interactions between objects
            gameLogics();
            // bottles and wall logic
            BottlesAndWallLogic();

        }
        //敌人数量为0，并且达到最大值，游戏成功
        if (EnemiesList.size() == 0 && enemyCount == maxEnemyCount) {
            MainWindow.state = GameConstants.GAME_SUCCESS;
        }

        //玩家生命消失/瓶子数为0，游戏失败
        if (playerList.size() == 0 || bottleList.size() == 0) {
            MainWindow.state = GameConstants.GAME_OVER;
        }

        if (MainWindow.gameModel==GameConstants.GAME_MODEL_SIGN_PLAYER && !playerOne.isAlive() || bottleList.size() == 0) {
            MainWindow.state = GameConstants.GAME_OVER;
        }

        if (playerList.size() > 0) {
            for (Role role : playerList) {
                if (role instanceof PlayerOne) {
                    PlayerOne one = (PlayerOne) role;
                    if (!one.isAlive()) {
                        playerList.remove(one);
                    }
                }
                if (role instanceof PlayerTwo) {
                    PlayerTwo two = (PlayerTwo) role;
                    if (!two.isAlive()) {
                        playerList.remove(two);
                    }
                }
            }
        }

    }

    // interactions between objects
    private void gameLogics() {
        // this is a way to increment across the array list data structure
        // see if they hit anything
        // using enhanced for-loop style as it makes it alot easier both code wise and
        // reading wise too

        for (Bullet Bullet : BulletList) {
            for (Enemy temp : EnemiesList) {
                if (Math.abs(temp.getCentre().getX() - Bullet.getCentre().getX()) < temp.getWidth()
                        && Math.abs(temp.getCentre().getY() - Bullet.getCentre().getY()) < temp.getHeight()) {
                    createBoom(temp);
                    AudioPlay.play("boom");
                    EnemiesList.remove(temp);
                    BulletList.remove(Bullet);
                }
            }
            for (Wall temp : wallList) {
                if (Math.abs(temp.getCentre().getX() - Bullet.getCentre().getX()) < temp.getWidth()
                        && Math.abs(temp.getCentre().getY() - Bullet.getCentre().getY()) < temp.getHeight()) {
                    wallList.remove(temp);
                    BulletList.remove(Bullet);

                }
            }
        }

        for (Bullet Bullet : enemyBulletsList) {

            if (playerOne.isAlive()&&Math.abs(playerOne.getCentre().getX() - Bullet.getCentre().getX()) < playerOne.getWidth()
                    && Math.abs(playerOne.getCentre().getY() - Bullet.getCentre().getY()) < playerOne.getHeight()) {
                playerOne.setAlive(false);
                createBoom(playerOne);
                AudioPlay.play("boom");
                playerList.remove(playerOne);
                enemyBulletsList.remove(Bullet);

            }
            if (playerTwo.isAlive()&&Math.abs(playerTwo.getCentre().getX() - Bullet.getCentre().getX()) < playerTwo.getWidth()
                    && Math.abs(playerTwo.getCentre().getY() - Bullet.getCentre().getY()) < playerTwo.getHeight()) {
                createBoom(playerTwo);
                AudioPlay.play("boom");
                playerTwo.setAlive(false);
                playerList.remove(playerTwo);
                enemyBulletsList.remove(Bullet);
            }

            for (Wall temp : wallList) {
                if (Math.abs(temp.getCentre().getX() - Bullet.getCentre().getX()) < temp.getWidth()
                        && Math.abs(temp.getCentre().getY() - Bullet.getCentre().getY()) < temp.getHeight()) {
                    wallList.remove(temp);
                    enemyBulletsList.remove(Bullet);

                }
            }
            for (Bottle temp : bottleList) {
                if (Math.abs(temp.getCentre().getX() - Bullet.getCentre().getX()) < temp.getWidth()
                        && Math.abs(temp.getCentre().getY() - Bullet.getCentre().getY()) < temp.getHeight()) {
                    createBoom(temp);
                    AudioPlay.play("boom");
                    bottleList.remove(temp);
                    enemyBulletsList.remove(Bullet);
                }
            }

        }
    }

    public void TankAndPlayerCollideMapTile() {
        PlayerOne playerOne = getPlayerOne();
        PlayerTwo playerTwo = getPlayerTwo();

        if (playerOne.isCollideTile(getWallList())) {
            playerOne.back();
        }

        if (playerTwo.isCollideTile(getWallList())) {
            playerTwo.back();
        }
        for (int i = 0; i < getEnemies().size(); i++) {
            Enemy enemy = getEnemies().get(i);
            if (enemy.isCollideTile(getWallList())) {
                enemy.back();
            }
        }
    }


    //create bottle
    private void BottlesAndWallLogic() {
        //最大瓶子数量
        Map<Integer, Integer> bottlePoint =new HashMap<>();

        List<Integer> listX = Arrays.asList(350, 500, 650, 800);
        List<Integer> listY = Arrays.asList(150, 300, 450, 600);

        while (bottleCount < 3) {

            int indexX = (int) (Math.random() * listX.size());
            int indexY = (int) (Math.random() * listY.size());
            //如果坐标一致则重新生成位置
            if (bottlePoint.containsKey(indexX) && bottlePoint.get(indexX)==indexY){
                continue;
            }
            bottlePoint.put(indexX,indexY);
            createBottlesAndWall(listX.get(indexX), listY.get(indexY));
            bottleCount++;

        }
    }

    // according to bottles location to create wall
    private void createBottlesAndWall(float x, float y) {
        for (int i = 1; i < 16; i++) {
            wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(250, i * 50, 0)));
            wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(900, i * 50, 0)));
        }

        bottleList.add(new Bottle(ImageUtil.bottle, 50, 50, new Point3f(x, y, 0)));
        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x - 50, y - 50, 0)));
        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x - 50, y, 0)));
        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x - 50, y + 50, 0)));

        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x, y - 50, 0)));
        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x, y + 50, 0)));

        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x + 50, y - 50, 0)));
        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x + 50, y, 0)));
        wallList.add(new Wall(ImageUtil.brick, 50, 50, new Point3f(x + 50, y + 50, 0)));

    }

    //when interactions create boom
    private void createBoom(GameObject position) {
        Boom boom = new Boom();
        boom.setCentre(position.getCentre());
        boomList.add(boom);
    }

    //create Enemy
    private void createEnemy() {
        Random random = new Random();
        int i = random.nextInt(5000);
        if (i < enemyRate && enemyCount < maxEnemyCount) {
            List<Integer> listX = Arrays.asList(100, 1100);
            int indexX = (int) (Math.random() * listX.size());
            Enemy enemy = new Enemy(ImageUtil.gunU, 50, 50,
                    new Point3f(listX.get(indexX), (float) Math.random() * 600 + 50, 0),
                    ImageUtil.gunU, ImageUtil.gunD, ImageUtil.gunL, ImageUtil.gunR);
            EnemiesList.add(enemy);
            enemyCount++;
            enemyRate++;
        }
    }


    //敌人自动移动和发射子弹逻辑
    private void enemyLogic() {
        // TODO Auto-generated method stub
        createEnemy();
        for (Enemy enemy : EnemiesList) {
            enemy.autoMove();
            if (enemy.isCanFire()) {
                enemyBulletsList.add(new Bullet(ImageUtil.bulletYellow, 32, 32,
                        new Point3f(enemy.getCentre().getX()+12, enemy.getCentre().getY()+12, 0.0f), enemy.getDirection()));
                enemy.fire();
            } else {
                //检查冷却时间
                enemy.checkCooling();
            }
        }
    }

    //bullet logic
    private void bulletLogic(List<Bullet> list) {

        // move bullets
        for (Bullet bullet : list) {
            // check to move them
            bulletMove(bullet.getDirection(), bullet.getCentre());
            // see if they hit anything
            if (bullet.getCentre().getX() == 0 || bullet.getCentre().getY() == 0 || bullet.getCentre().getX() == 1150
                    || bullet.getCentre().getY() == 750) {
                list.remove(bullet);

            }
        }
    }

    //子弹移动方向
    private void bulletMove(Direction direction, Point3f centre) {
        switch (direction) {
            case UP:
                centre.ApplyVector(new Vector3f(0, -5, 0));
                break;
            case DOWN:
                centre.ApplyVector(new Vector3f(0, 5, 0));
                break;
            case LEFT:
                centre.ApplyVector(new Vector3f(-5, 0, 0));
                break;
            case RIGET:
                centre.ApplyVector(new Vector3f(5, 0, 0));
                break;
            default:
                break;
        }
    }

    //玩家1键盘控制
    private void playerOneLogic() {

        // smoother animation is possible if we make a target position // done but may
        // try to change things for students

        // check for movement and if you fired a bullet

        if (controller.isKeyAPressed()) {
            playerOne.setTexture(playerOne.getLeftImg());
            playerOne.setDirection(Direction.LEFT);
            playerOne.getCentre().ApplyVector(new Vector3f(-3, 0, 0));

        } else if (controller.isKeyDPressed()) {
            playerOne.setTexture(playerOne.getRightImg());
            playerOne.setDirection(Direction.RIGET);
            playerOne.getCentre().ApplyVector(new Vector3f(3, 0, 0));

        } else if (controller.isKeyWPressed()) {
            playerOne.setTexture(playerOne.getUpImg());
            playerOne.setDirection(Direction.UP);
            playerOne.getCentre().ApplyVector(new Vector3f(0, -3, 0));

        } else if (controller.isKeySPressed()) {
            playerOne.setTexture(playerOne.getDownImg());
            playerOne.setDirection(Direction.DOWN);
            playerOne.getCentre().ApplyVector(new Vector3f(0, 3, 0));
        }

        if (controller.isKeySpacePressed()) {
            CreatePlayerOneBullet(playerOne.getDirection());
            controller.setKeySpacePressed(false);
        }


    }

